@app.post("/research", response_model=ResearchResponse)
async def start_research(request: ResearchRequest, background_tasks: BackgroundTasks):
    import datetime
    task_id = str(uuid.uuid4())

    with sqlite3.connect(DB_PATH) as conn:
        conn.execute(
            "INSERT INTO tasks (task_id, status, result) VALUES (?, ?, ?)",
            (task_id, "processing", json.dumps({
                "query": request.query,
                "logs": [
                    f"[{datetime.datetime.now().strftime('%H:%M:%S')}] INITIALIZING COGNITIVE INTERFACE...",
                    f"[{datetime.datetime.now().strftime('%H:%M:%S')}] TASK REGISTERED IN PERSISTENT NODE: {task_id}"
                ],
                "current_step": "idle",
                "search_queries": [],
                "sources": [],
                "final_report": ""
            }))
        )

    # Run the agent in the background
    background_tasks.add_task(run_research, task_id, request.query, request.template, request.webhook_url)

    return ResearchResponse(task_id=task_id, status="processing")

@app.get("/status/{task_id}")
async def get_status(task_id: str):
    with sqlite3.connect(DB_PATH) as conn:
        cursor = conn.execute("SELECT status, result FROM tasks WHERE task_id = ?", (task_id,))
        row = cursor.fetchone()

    if not row:
        raise HTTPException(status_code=404, detail="Task not found")

    status, result_json = row
    result = json.loads(result_json) if result_json else None

    return {
        "status": status,
        "result": result
    }

@app.get("/history")
async def get_history():
    try:
        with sqlite3.connect(DB_PATH) as conn:
            cursor = conn.execute("SELECT task_id, status, result FROM tasks ORDER BY rowid DESC")
            rows = cursor.fetchall()
        
        history = []
        for row in rows:
            task_id, status, result_json = row
            result = json.loads(result_json) if result_json else {}
            history.append({
                "task_id": task_id,
                "status": status,
                "query": result.get("query") or "Unknown Query",
                "result": result
            })
        return history
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

@app.get("/export/{task_id}")
async def export_task(task_id: str, format: str = "html", theme: str = "tech"):
    with sqlite3.connect(DB_PATH) as conn:
        cursor = conn.execute("SELECT status, result FROM tasks WHERE task_id = ?", (task_id,))
        row = cursor.fetchone()
        
    if not row:
        raise HTTPException(status_code=404, detail="Task not found")
        
    status, result_json = row
    if status != "completed" or not result_json:
        raise HTTPException(status_code=400, detail="Research is not completed yet")
        
    result = json.loads(result_json)
    query = result.get("query", "Research Query")
    report = result.get("final_report", "")
    
    if format == "slides":
        slides_md = convert_report_to_marp_slides(query, report)
        return PlainTextResponse(content=slides_md, media_type="text/plain")
    else:
        html_page = get_themed_html(query, report, theme)
        return HTMLResponse(content=html_page)

