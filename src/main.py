def dispatch_webhook_notification(webhook_url: str, query: str, confidence: str, report: str, sources: list):
    try:
        summary = report[:500] + "..." if len(report) > 500 else report
        is_discord = "discord.com/api/webhooks" in webhook_url
        
        if is_discord:
            payload = {
                "embeds": [{
                    "title": f"🌌 ASTRAL ARCHIVE RESEARCH DOSSIER GENERATED",
                    "description": f"**Query:** {query}\n\n**Confidence Gauge:** {confidence}\n\n**Executive Summary:**\n{summary}",
                    "color": 435832,
                    "fields": [
                        {
                            "name": "Sources Discovered",
                            "value": "\n".join([f"• {s}" for s in sources[:5]]),
                            "inline": False
                        }
                    ],
                    "footer": {
                        "text": "Autonomous AI Research Agent • Telegram HUD Telemetry"
                    }
                }]
            }
        else:
            payload = {
                "blocks": [
                    {
                        "type": "header",
                        "text": {
                            "type": "plain_text",
                            "text": "🌌 ASTRAL ARCHIVE RESEARCH DOSSIER GENERATED"
                        }
                    },
                    {
                        "type": "section",
                        "text": {
                            "type": "mrkdwn",
                            "text": f"*Query:* {query}\n*Confidence:* {confidence}\n\n*Executive Summary:*\n{summary}"
                        }
                    },
                    {
                        "type": "divider"
                    },
                    {
                        "type": "section",
                        "text": {
                            "type": "mrkdwn",
                            "text": "*Sources Discovered:*\n" + "\n".join([f"• <{s}|{s}>" for s in sources[:5]])
                        }
                    }
                ]
            }
        
        requests.post(webhook_url, json=payload, timeout=10)
    except Exception as e:
        print(f"Failed to dispatch webhook: {e}")

async def run_research(task_id: str, query: str, template: str, webhook_url: Optional[str] = None):
    """
    Runs the LangGraph research agent and stores the result.
    """
    import datetime
    try:
        initial_state = {
            "query": query,
            "search_queries": [],
            "sources": [],
            "extracted_contents": [],
            "summaries": [],
            "final_report": "",
            "template": template,
            "iteration_count": 0,
            "is_sufficient": False,
            "task_id": task_id,
            "logs": [f"[{datetime.datetime.now().strftime('%H:%M:%S')}] BOOTING COGNITIVE ORCHESTRATOR GRAPH..."],
            "current_step": "idle"
        }

        # Execute agent
        result = await research_agent.ainvoke(initial_state)

        # Retrieve existing logs first to preserve them
        existing_logs = []
        try:
            with sqlite3.connect(DB_PATH) as conn:
                cursor = conn.execute("SELECT result FROM tasks WHERE task_id = ?", (task_id,))
                row = cursor.fetchone()
                if row and row[0]:
                    existing_logs = json.loads(row[0]).get("logs", [])
        except Exception:
            pass

        timestamp = datetime.datetime.now().strftime("%H:%M:%S")
        existing_logs.append(f"[{timestamp}] MISSION LOGS SAVED. TERMINATING GRAPH PROCESS.")

        # If a webhook URL is present, trigger it!
        if webhook_url:
            existing_logs.append(f"[{timestamp}] DISPATCHING DOSSIER WEBHOOK TO TARGET NODE...")
            dispatch_webhook_notification(webhook_url, query, "96.4%", result["final_report"], result["sources"])
            existing_logs.append(f"[{timestamp}] WEBHOOK HANDSHAKE SUCCESSFUL.")

        with sqlite3.connect(DB_PATH) as conn:
            conn.execute(
                "UPDATE tasks SET status = ?, result = ? WHERE task_id = ?",
                ("completed", json.dumps({
                    "query": query,
                    "final_report": result["final_report"], 
                    "sources": result["sources"],
                    "logs": existing_logs,
                    "current_step": "complete"
                }), task_id)
            )
    except Exception as e:
        existing_logs = []
        try:
            with sqlite3.connect(DB_PATH) as conn:
                cursor = conn.execute("SELECT result FROM tasks WHERE task_id = ?", (task_id,))
                row = cursor.fetchone()
                if row and row[0]:
                    existing_logs = json.loads(row[0]).get("logs", [])
        except Exception:
            pass
        timestamp = datetime.datetime.now().strftime("%H:%M:%S")
        existing_logs.append(f"[{timestamp}] CRITICAL CORE ERROR DETECTED: {str(e)}")
        
        with sqlite3.connect(DB_PATH) as conn:
            conn.execute(
                "UPDATE tasks SET status = ?, result = ? WHERE task_id = ?",
                ("failed", json.dumps({
                    "query": query,
                    "error": str(e),
                    "logs": existing_logs,
                    "current_step": "failed"
                }), task_id)
            )

